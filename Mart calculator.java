package experiment.pkg2;
import java.util.*;


public class Experiment2 {
 
    public static void main(String[] args) {
       
       double totalpay;
       Scanner spec=new Scanner(System.in);
       System.out.println("number of units purchased ?");
       int uni =spec.nextInt();
       System.out.println("cost of each unit?");
       int cos=spec.nextInt();
       double amt=uni*cos;
       double dis;
       if(amt>=10000){
           dis=amt-amt*10/100;
       }else 
           dis=0;
       totalpay=amt-dis;
       System.out.println("the amount payable before discount"+amt);
       System.out.println("discount offered"+dis);
       System.out.println("the amount payable after discount"+totalpay);
       
    
}
}
